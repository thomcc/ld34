const Consts = require('./constants');

class TileInfo {
	constructor(edges, tx=0, ty=0) {
		this.id = -1;
		this.edges = edges.map(({start:{x:sx, y:sy}, end:{x:ex, y:ey}}) =>
			new LineSegment(new Vec2(sx+tx, sy+ty), new Vec2(ex+tx, ey+ty)));
	}

	offsetBy(x, y) {
		return new TileInfo(this.edges, x, y);
	}
}

