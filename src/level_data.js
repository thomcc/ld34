function polyTile(...pts) {
	return [
		polygon(...pts),
		polygon(...pts.map(pt => pt.rotated90(TILE_MID))),
		polygon(...pts.map(pt => pt.rotated180(TILE_MID))),
		polygon(...pts.map(pt => pt.rotated270(TILE_MID))),
	];
}


